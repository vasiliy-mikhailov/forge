#!/usr/bin/env bash
# load-active-model.sh — render phase-b-business-architecture/org-units/wiki-compiler/.env.active-model
# from the entry in configs/models.yml referenced by the active selector
# in forge/.env: INFERENCE_ACTIVE_MODEL_ID.
#
# See docs/adr/0008-model-registry-single-source-of-truth.md for design.
#
# Idempotent. Safe to run repeatedly. Designed to be chained from `make up`
# in the compiler lab Makefile so compose always reads a fresh
# .env.active-model before bringing the container up.

set -euo pipefail

HERE=$(cd -- "$(dirname -- "${BASH_SOURCE[0]}")/.." &>/dev/null && pwd)
FORGE_ROOT=$(git -C "$HERE" rev-parse --show-toplevel)

ACTIVE_ID=$(grep -E '^INFERENCE_ACTIVE_MODEL_ID=' "$FORGE_ROOT/.env" | head -1 | cut -d= -f2- || true)
if [[ -z "${ACTIVE_ID:-}" ]]; then
  echo "ERROR: INFERENCE_ACTIVE_MODEL_ID is not set in $FORGE_ROOT/.env" >&2
  echo "       Add a line like: INFERENCE_ACTIVE_MODEL_ID=qwen3.6-27b-fp8" >&2
  echo "       Available ids: see phase-b-business-architecture/org-units/wiki-compiler/configs/models.yml" >&2
  exit 2
fi

REGISTRY="$HERE/configs/models.yml"
OUT="$HERE/.env.active-model"

if [[ ! -f "$REGISTRY" ]]; then
  echo "ERROR: model registry not found at $REGISTRY" >&2
  exit 2
fi

python3 - "$ACTIVE_ID" "$REGISTRY" "$OUT" <<'PY'
import sys, yaml

active_id, registry, out = sys.argv[1], sys.argv[2], sys.argv[3]

with open(registry, encoding="utf-8") as f:
    data = yaml.safe_load(f)

m = next((x for x in data.get("models", []) if x.get("id") == active_id), None)
if m is None:
    available = ", ".join(x.get("id", "?") for x in data.get("models", []))
    sys.exit(f"ERROR: model id '{active_id}' not found in registry.\n"
             f"       Available: {available}")

# Compose-friendly defaults for fields that may be missing.
def safe(v, default):
    return default if v is None else v

rope = m.get("rope_scaling") or {}
chat = m.get("chat_template_kwargs") or {}

lines = [
    "# auto-generated by phase-b-business-architecture/org-units/wiki-compiler/bin/load-active-model.sh",
    "# DO NOT EDIT BY HAND. Edit configs/models.yml or forge/.env (active id) instead.",
    f"# active id: {active_id}",
    "",
    f"MODEL_ID={m['id']}",
    f"MODEL_HF={m['hf']}",
    f"MODEL_SERVED_NAME={m['served_name']}",
    f"MODEL_QUANTIZATION={safe(m.get('quantization'), '')}",
    f"MODEL_KV_CACHE_DTYPE={m.get('kv_cache_dtype', 'fp8')}",
    f"MODEL_MAX_MODEL_LEN={m['max_model_len']}",
    f"MODEL_ROPE_TYPE={rope.get('rope_type', 'yarn')}",
    f"MODEL_ROPE_FACTOR={rope.get('factor', 1.0)}",
    f"MODEL_ROPE_ORIG_MAX={rope.get('original_max_position_embeddings', m['max_model_len'])}",
    f"MODEL_TOOL_CALL_PARSER={m.get('tool_call_parser', 'hermes')}",
    f"MODEL_REASONING_PARSER={safe(m.get('reasoning_parser'), '')}",
    f"MODEL_ENABLE_THINKING={'true' if chat.get('enable_thinking', False) else 'false'}",
    f"MODEL_TENSOR_PARALLEL_SIZE={m.get('tensor_parallel_size', 1)}",
    f"MODEL_GPU_MEMORY_UTILIZATION={m.get('gpu_memory_utilization', 0.92)}",
]

with open(out, "w", encoding="utf-8") as f:
    f.write("\n".join(lines) + "\n")

print(f"[load-active-model] wrote {out}")
print(f"  id={m['id']}  hf={m['hf']}  max_model_len={m['max_model_len']}  "
      f"rope_factor={rope.get('factor', 1.0)}  tp={m.get('tensor_parallel_size', 1)}")
PY
